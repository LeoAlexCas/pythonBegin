# viendo el type de los strings y como se pueden declarar
print(type("Hello World")) 
print(type('Hello World'))
print(type("""Hello World"""))
print(type('''Hello World'''))

# Tipos numericos
print(type(30))
print(type(30.1))

#boolean
print(type(False))
print(type(True))

#list
print(type([1, 2, 3, 4]))
print(type(["a", "b", "c"]))

#tuplas
print(type((1, 100)))

#Dictionaries
print(type({
    "name": "leo",
    "nick": "leos"
}))
